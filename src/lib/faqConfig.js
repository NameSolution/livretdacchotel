// Fonction pour récupérer les FAQ dans une langue donnée
export const getFAQForLanguage = (language) => {
  return faqStructure.map((faq, index) => ({
    id: `q${index + 1}`,
    question: faq.translations[language]?.question || faq.translations.fr.question,
    answer: faq.translations[language]?.answer || faq.translations.fr.answer
  })).filter(faq => faq.question && faq.answer); // Filtrer les FAQ vides
};
